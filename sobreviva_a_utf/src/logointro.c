#include <curses.h>
#include <unistd.h>

void animacaoLinha(const char *texto[], int linhas, int delay) {
    clear();

    for (int i = 0; i < linhas; i++) {
        mvprintw(i, 0, "%s", texto[i]);
        refresh();
        usleep(delay);
    }

    refresh();
    sleep(3);
    endwin();
}

